	<h2 class="text-3xl">Doomscrolling and Mental Health</h2>
	<p>
		Doomscrolling is not only a bad habit; it can also be a sign of underlying mental health
		challenges, such as depression or anxiety. Spending hours scrolling through news and content can
		exacerbate feelings of hopelessness, stress, and isolation.
	</p>
	<p>
		It’s important to recognize that doomscrolling can affect both your mental and emotional
		well-being. If you find yourself feeling overwhelmed or unable to stop, it’s okay to ask for
		help. Many people struggle with similar habits, but with support, it’s possible to take control
		of your digital life and mental health.
	</p>

	<!-- Don't Be Afraid to Ask for Help -->
	<h2>Don't Be Afraid to Ask for Help</h2>
	<p>
		Mental health challenges can feel isolating, but you are not alone. Whether you're experiencing
		mild symptoms or more severe distress, reaching out for support is a vital step. Speaking with a
		professional or someone you trust can help you process emotions, find coping mechanisms, and
		regain control.
	</p>
	<p>
		There’s no shame in asking for help. Depression, anxiety, and other mental health conditions are
		common and treatable. Reaching out for assistance shows strength, not weakness.
	</p>

	<!-- What You Can Do Right Now -->
	<h2>What You Can Do Right Now</h2>
	<p>
		If doomscrolling is affecting your mood and productivity, here are some immediate steps you can
		take:
	</p>
	<ul>
		<li>
			Set Time Limits: Use the Stop Doomscroll extension to limit the time spent on negative or
			overwhelming content.
		</li>
		<li>
			Practice Mindfulness: Take a few minutes to breathe deeply, clear your mind, and focus on
			positive or neutral activities.
		</li>
		<li>Reach Out: Talk to a friend, family member, or professional about what you’re feeling.</li>
	</ul>

	<p>
		Remember, asking for help is a sign of strength, not weakness. You deserve support, and it’s
		available to you.
	</p>

